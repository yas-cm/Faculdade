public class Empregado{
   private String nome;
   private String cargo;
   private int idade;
   
   public Empregado(String n, String c, int i){
      n = nome;
      c = cargo;
      i = idade;  
   }
   public String getNome() {return nome;}
   public String getCargo() {return cargo;}
   public int getIdade() {return idade;}
   
}